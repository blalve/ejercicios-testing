package org.example;

public class Calcu {
    public static float suma (Float a, Float b){
        return a+b;
    }
    public static float resta (float a, float b){
        return a-b;
    }
    public static float multi (float a, float b){
        return a*b;
    }
    public static float divi (float a, float b){
        return a/b;
    }
}
